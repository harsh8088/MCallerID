package com.example.harsh.mcallerid;

class CallerModel {
    private String conNames;
    private String conNumbers;
    private String conTime;
    private String conDate;
    private String conType;


    public String getConNames() {
        return conNames;
    }

    public void setConNames(String conNames) {
        this.conNames = conNames;
    }

    public String getConNumbers() {
        return conNumbers;
    }

    public void setConNumbers(String conNumbers) {
        this.conNumbers = conNumbers;
    }

    public String getConTime() {
        return conTime;
    }

    public void setConTime(String conTime) {
        this.conTime = conTime;
    }

    public String getConDate() {
        return conDate;
    }

    public void setConDate(String conDate) {
        this.conDate = conDate;
    }

    public String getConType() {
        return conType;
    }

    public void setConType(String conType) {
        this.conType = conType;
    }
}
